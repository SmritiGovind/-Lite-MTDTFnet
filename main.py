def train(model, dataloader, criteria, device, optimizer, scheduler, epoch, num_epochs):
    loss_epoch = 0.
    criterion_perceptual = MS_SSIMloss()

    for batch_idx, (LR_img_l, LR_img_r, HR_img_l, name, _, _) in enumerate(dataloader):
    
        optimizer.zero_grad()
        LR_left, LR_right = LR_img_l.to(device).float(), LR_img_r.to(device).float()
        HR_left = HR_img_l.to(device).float()

        SR_left = model(LR_left, LR_right)

        #loss = criteria(SR_left, HR_left)
        loss_char =  criteria(SR_left, HR_left)
        loss_perceptual = criterion_perceptual(SR_left, HR_left)
        # loss_DL = torch.mean(torch.abs(wrapped_SR - LR_right))
        loss = loss_char + 0.1 * loss_perceptual

        # Backpropagation
        loss.backward()
        optimizer.step()
        scheduler.step()

        loss_epoch += loss.item()

        # live progress percentage
        progress = 100.0 * (batch_idx + 1) / len(dataloader)
        print(f"\rEpoch {epoch}/{num_epochs}: {progress:.2f}% completed", end="")

    print()  # new line after finishing one epoch
    loss_epoch /= len(dataloader)
    lr_epoch = scheduler.get_last_lr()[0]
    return loss_epoch, lr_epoch



