class QuantDeformConv2d(nn.Module):
    def __init__(self, in_channels, out_channels, kernel_size=3, bias=True):
        super().__init__()

        self.kernel_size = kernel_size
        self.in_channels = in_channels
        self.out_channels = out_channels
        self.N = kernel_size * kernel_size

        self.weight = nn.Parameter(
            torch.randn(out_channels, in_channels, kernel_size, kernel_size)
        )

        self.bias = nn.Parameter(torch.zeros(out_channels)) if bias else None


    def forward(self, x, offset, mask=None):

        B, C, H, W = x.shape

        # reshape offsets
        offset = offset.view(B, self.N, 2, H, W).permute(0,3,4,1,2)

        if mask is not None:
            mask = mask.view(B,self.N,H,W).permute(0,2,3,1)


        # base grid
        grid_y, grid_x = torch.meshgrid(
            torch.arange(H, device=x.device, dtype=x.dtype),
            torch.arange(W, device=x.device, dtype=x.dtype),
            indexing="ij"
        )

        base_grid = torch.stack((grid_x,grid_y),dim=-1)
        base_grid = base_grid.unsqueeze(0).unsqueeze(3)


        sampling_locations = base_grid + offset

        sampling_locations[...,0] = sampling_locations[...,0]/(W-1)*2-1
        sampling_locations[...,1] = sampling_locations[...,1]/(H-1)*2-1

        sampling_locations = sampling_locations.clamp(-1+1e-4,1-1e-4)


        # Allocate final tensor directly
        x_sampled = torch.empty(
            B,
            C*self.N,
            H,
            W,
            device=x.device,
            dtype=x.dtype
        )


        # grid sample per kernel position
        with torch.backends.cudnn.flags(enabled=False):

            for n in range(self.N):

                grid_n = sampling_locations[...,n,:]

                sampled_n = F.grid_sample(
                    x,
                    grid_n,
                    mode="bilinear",
                    padding_mode="zeros",
                    align_corners=True
                )

                if mask is not None:
                    sampled_n = sampled_n * mask[...,n].unsqueeze(1)


                x_sampled[:, n*C:(n+1)*C,:,:] = sampled_n


        weight = self.weight.view(self.out_channels,-1,1,1)

        out = F.conv2d(x_sampled,weight,bias=self.bias)

        return out



