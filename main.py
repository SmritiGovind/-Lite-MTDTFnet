class TeagerEnergyLayer(nn.Module):
    def __init__(self, in_channels):
        super().__init__()
        self._norm = nn.InstanceNorm2d(in_channels, affine=True)

    def forward(self, x):
        # Pad for boundary conditions
        x_pad = torch.cat((x[:, :, :1], x, x[:, :, -1:]), dim=2)
        x_pad = torch.cat((x_pad[:, :, :, :1], x_pad, x_pad[:, :, :, -1:]), dim=3)

        teo_output = (
            x_pad[:, :, 1:-1, 1:-1] ** 2
            - x_pad[:, :, :-2, 1:-1] * x_pad[:, :, 2:, 1:-1]
            - x_pad[:, :, 1:-1, :-2] * x_pad[:, :, 1:-1, 2:]
        )
        teo_output = self._norm(teo_output)
        return teo_output

