class DeformAlignBlock(nn.Module):
    def __init__(self, in_channels, kernel_size=3, modulation=False):
        super().__init__()
        self.kernel_size = kernel_size
        self.N = kernel_size * kernel_size
        self.modulation = modulation

        self.offset_conv = nn.Conv2d(in_channels * 2, 2 * self.N, kernel_size=3, padding=1)
        nn.init.constant_(self.offset_conv.weight, 0)
        nn.init.constant_(self.offset_conv.bias, 0)

        if modulation:
            self.mask_conv = nn.Conv2d(in_channels * 2, self.N, kernel_size=3, padding=1)
            nn.init.constant_(self.mask_conv.weight, 0)
            nn.init.constant_(self.mask_conv.bias, 0)

        self.deform_conv = QuantDeformConv2d(in_channels, in_channels, kernel_size)
        self.post = nn.Sequential(
            nn.Conv2d(in_channels, in_channels, kernel_size=3, padding=1),
            nn.GELU()
        )

        self.teager_layer = TeagerEnergyLayer(in_channels)

    def forward(self, lr_feat, ref_feat, teager):
        B, C, H_lr, W_lr = lr_feat.shape
        B, C, H_ref, W_ref = ref_feat.shape

        if teager == True:
                # Apply Teager Energy Operator
                teager_ref_feat = self.teager_layer(ref_feat)
                teager_ref_up = F.interpolate(teager_ref_feat, size=(H_lr, W_lr), mode="bilinear", align_corners=False)
        else:
                teager_ref_up = F.interpolate(ref_feat, size=(H_lr, W_lr), mode="bilinear", align_corners=False)

        fused = torch.cat([teager_ref_up, lr_feat], dim=1)
        offset = torch.tanh(self.offset_conv(fused)) * 3.0

        if self.modulation:
            mask = torch.sigmoid(self.mask_conv(fused))
            aligned = self.deform_conv(lr_feat, offset, mask)
        else:
            aligned = self.deform_conv(lr_feat, offset)

        out = aligned + lr_feat
        out = self.post(out)
        return out


