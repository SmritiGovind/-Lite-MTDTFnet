def multiscale(kernel, target_kernel_size):
    """Pad kernel to target size (left, right, top, bottom)."""
    H_pad = (target_kernel_size - kernel.size(2)) // 2
    W_pad = (target_kernel_size - kernel.size(3)) // 2
    return F.pad(kernel, [W_pad, W_pad, H_pad, H_pad])


