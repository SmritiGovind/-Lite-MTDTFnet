def extract_patches(LR_img, HR_img, Ref_img, patch_size=108, scale=2, stride=108):
    """
    Extracts all patches from LR/HR/Ref images with stride.
    Returns list of (LR_patch, HR_patch, Ref_patch, (Hx,Hy), Hp)
    """
    L_h, L_w = LR_img.shape[:2]
    H_h, H_w = HR_img.shape[:2]

    patches = []
    for y in range(0, L_h - patch_size + 1, stride):
        for x in range(0, L_w - patch_size + 1, stride):
            H_x, H_y = scale * x, scale * y
            H_p = scale * patch_size

            patch_LR = LR_img[y:y + patch_size, x:x + patch_size, :]
            patch_HR = HR_img[H_y:H_y + H_p, H_x:H_x + H_p, :]
            patch_Ref = Ref_img[y:y + patch_size, x:x + patch_size, :]

            patches.append((patch_LR, patch_HR, patch_Ref, (H_x, H_y), H_p))
    return patches

def random_crop(LR_img, HR_img, crop_size, SR_rate):
    # check the shape

    LR_h, LR_w = LR_img.shape[:2]
    HR_h, HR_w = HR_img.shape[:2]
    print('np.round_(LR_h * SR_rate)=', np.round_(LR_h * SR_rate), ', HR_h=', HR_h)
    assert np.round_(LR_h * SR_rate) == HR_h and np.round_(LR_w * SR_rate) == HR_w, 'SR_rate is not correct for LR and HR image'
    # check the crop size
    new_LR_h, new_LR_w = crop_size
    assert new_LR_h <= LR_h and new_LR_w <= LR_w, 'crop_size is too large'

    y1 = random.randint(0, LR_h - new_LR_h)
    x1 = random.randint(0, LR_w - new_LR_w)

    LR_crop = LR_img[y1:y1 + new_LR_h, x1:x1 + new_LR_w, :]
    HR_crop = HR_img[SR_rate * y1:SR_rate * (y1 + new_LR_h), SR_rate * x1:SR_rate * (x1 + new_LR_w), :]

    return LR_crop, HR_crop


