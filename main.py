class SeqConv3x3(nn.Module):
    def __init__(self, seq_type, inp_planes, out_planes, depth_multiplier):
        super(SeqConv3x3, self).__init__()

        self.type = seq_type
        self.inp_planes = inp_planes
        self.out_planes = out_planes

        # -------------------------------
        # 1x1 + 3x3 Standard Reparam Path
        # -------------------------------

        if self.type == 'conv1x1-conv3x3':
            self.mid_planes = int(out_planes * depth_multiplier)
            conv0 = torch.nn.Conv2d(self.inp_planes, self.mid_planes, kernel_size=1, padding=0)
            self.k0 = conv0.weight
            self.b0 = conv0.bias

            conv1 = torch.nn.Conv2d(self.mid_planes, self.out_planes, kernel_size=3)
            self.k1 = conv1.weight
            self.b1 = conv1.bias

        # -------------------------------
        # Teager + Sobel X
        # -------------------------------
        elif self.type == 'conv1x1TeagerSobelx':
            conv0 = torch.nn.Conv2d(self.inp_planes, self.out_planes, kernel_size=1, padding=0)
            self.k0 = conv0.weight
            self.b0 = conv0.bias

            # init scale & bias
            scale = torch.randn(size=(self.out_planes, 1, 1, 1)) * 1e-3
            self.scale = nn.Parameter(scale)
            bias = torch.randn(self.out_planes) * 1e-3
            bias = torch.reshape(bias, (self.out_planes,))
            self.bias = nn.Parameter(bias)
            # init mask
            self.mask = torch.zeros((self.out_planes, 1, 3, 3), dtype=torch.float32)
            for i in range(self.out_planes):
                self.mask[i, 0, 1, 1] = 1.0      # centre excitation to mimic All pass Filter
                self.mask[i, 0, 0, 1] = -0.5
                self.mask[i, 0, 2, 1] = -0.5

                # SobelX horizontal gradient (stronger weights)
                self.mask[i, 0, 0, 0] = -1.0       # top-left
                self.mask[i, 0, 0, 2] = 1.0        # top-right
                self.mask[i, 0, 1, 0] = -2.0       # mid-left
                self.mask[i, 0, 1, 2] = 2.0        # mid-right
                self.mask[i, 0, 2, 0] = -1.0       # bot-left
                self.mask[i, 0, 2, 2] = 1.0        # bot-right (+Teager)

            self.mask = nn.Parameter(data=self.mask, requires_grad=False)

        # -------------------------------
        # Teager + Sobel Y
        # -------------------------------
        elif self.type == 'conv1x1TeagerSobely':
            conv0 = torch.nn.Conv2d(self.inp_planes, self.out_planes, kernel_size=1, padding=0)
            self.k0 = conv0.weight
            self.b0 = conv0.bias

            # init scale & bias
            scale = torch.randn(size=(self.out_planes, 1, 1, 1)) * 1e-3
            self.scale = nn.Parameter(torch.FloatTensor(scale))

            bias = torch.randn(self.out_planes) * 1e-3
            bias = torch.reshape(bias, (self.out_planes,))
            self.bias = nn.Parameter(torch.FloatTensor(bias))
            # init mask
            self.mask = torch.zeros((self.out_planes, 1, 3, 3), dtype=torch.float32)
            for i in range(self.out_planes):

                self.mask[i, 0, 1, 1] = 1.0      # centre excitation to mimic All pass Filter
                self.mask[i, 0, 1, 0] = -0.5
                self.mask[i, 0, 1, 2] = -0.5
                
                # SobelY horizontal gradient (stronger weights)
                self.mask[i, 0, 0, 0] = 1.0
                self.mask[i, 0, 0, 1] = 2.0
                self.mask[i, 0, 0, 2] = 1.0
                self.mask[i, 0, 2, 0] = -1.0
                self.mask[i, 0, 2, 1] = -2.0
                self.mask[i, 0, 2, 2] = -1.0

            self.mask = nn.Parameter(data=self.mask, requires_grad=False)

        # -------------------------------
        # Laplacian
        # -------------------------------
        elif self.type == 'conv1x1-laplacian':
            conv0 = torch.nn.Conv2d(self.inp_planes, self.out_planes, kernel_size=1, padding=0)
            self.k0 = conv0.weight
            self.b0 = conv0.bias

            # init scale & bias
            scale = torch.randn(size=(self.out_planes, 1, 1, 1)) * 1e-3
            self.scale = nn.Parameter(torch.FloatTensor(scale))

            bias = torch.randn(self.out_planes) * 1e-3
            bias = torch.reshape(bias, (self.out_planes,))
            self.bias = nn.Parameter(torch.FloatTensor(bias))
            # init mask
            self.mask = torch.zeros((self.out_planes, 1, 3, 3), dtype=torch.float32)
            for i in range(self.out_planes):
                self.mask[i, 0, 0, 1] = 1.0
                self.mask[i, 0, 1, 0] = 1.0
                self.mask[i, 0, 1, 2] = 1.0
                self.mask[i, 0, 2, 1] = 1.0
                self.mask[i, 0, 1, 1] = -4.0
            self.mask = nn.Parameter(data=self.mask, requires_grad=False)
        else:
            raise ValueError('the type of seqconv is not supported!')

    def forward(self, x):
        if self.type == 'conv1x1-conv3x3':
            # conv-1x1
            y0 = F.conv2d(input=x, weight=self.k0, bias=self.b0, stride=1)

            # explicitly padding with bias
            y0 = F.pad(y0, (1, 1, 1, 1), 'constant', 0)
            b0_pad = self.b0.view(1, -1, 1, 1)

            y0[:, :, 0:1, :] = b0_pad
            y0[:, :, -1:, :] = b0_pad
            y0[:, :, :, 0:1] = b0_pad
            y0[:, :, :, -1:] = b0_pad
            # conv-3x3
            y1 = F.conv2d(input=y0, weight=self.k1, bias=self.b1, stride=1)
        else:
            y0 = F.conv2d(input=x, weight=self.k0, bias=self.b0, stride=1)
            # explicitly padding with bias
            y0 = F.pad(y0, (1, 1, 1, 1), 'constant', 0)
            b0_pad = self.b0.view(1, -1, 1, 1)
            y0[:, :, 0:1, :] = b0_pad
            y0[:, :, -1:, :] = b0_pad
            y0[:, :, :, 0:1] = b0_pad
            y0[:, :, :, -1:] = b0_pad
            # conv-3x3
            y1 = F.conv2d(input=y0, weight=self.scale * self.mask, bias=self.bias, stride=1, groups=self.out_planes)
        return y1

    def rep_params(self):
        device = self.k0.get_device()
        if device < 0:
            device = None

        if self.type == 'conv1x1-conv3x3':
            # re-param conv kernel
            RK = F.conv2d(input=self.k1, weight=self.k0.permute(1, 0, 2, 3))
            # re-param conv bias
            RB = torch.ones(1, self.mid_planes, 3, 3, device=device) * self.b0.view(1, -1, 1, 1)
            RB = F.conv2d(input=RB, weight=self.k1).view(-1,) + self.b1
        else:
            tmp = self.scale * self.mask
            k1 = torch.zeros((self.out_planes, self.out_planes, 3, 3), device=device)
            for i in range(self.out_planes):
                k1[i, i, :, :] = tmp[i, 0, :, :]
            b1 = self.bias
            # re-param conv kernel
            RK = F.conv2d(input=k1, weight=self.k0.permute(1, 0, 2, 3))
            # re-param conv bias
            RB = torch.ones(1, self.out_planes, 3, 3, device=device) * self.b0.view(1, -1, 1, 1)
            RB = F.conv2d(input=RB, weight=k1).view(-1,) + b1
        return RK, RB

class MFDB(nn.Module):
    def __init__(self, inp_planes, out_planes, deploy, depth_multiplier=None, act_type='prelu', with_idt = False, with_13=False, gv=False):
        super(MFDB, self).__init__()

        self.deploy = deploy
        self.act_type = act_type

        self.inp_planes = inp_planes
        self.out_planes = out_planes

        self.gv = gv

        if depth_multiplier is None:
            self.depth_multiplier = 1.0
        else:
            self.depth_multiplier = depth_multiplier   # For mobilenet, it is better to have 2X internal channels

        if deploy:
            self.rep_conv = nn.Conv2d(in_channels=inp_planes, out_channels=out_planes, kernel_size=3, stride=1,
                                      padding=1, bias=True)
        else:
            self.with_13 = with_13
            if with_idt and (self.inp_planes == self.out_planes):
                self.with_idt = True
            else:
                self.with_idt = False

            self.rep_conv = nn.Conv2d(self.inp_planes, self.out_planes, kernel_size=3, padding=1)
            self.conv1x1 = nn.Conv2d(self.inp_planes, self.out_planes, kernel_size=1, padding=0)
            self.conv1x1_3x3 = SeqConv3x3('conv1x1-conv3x3', self.inp_planes, self.out_planes, self.depth_multiplier)
            self.conv1x1_teo_sbx = SeqConv3x3('conv1x1TeagerSobelx', self.inp_planes, self.out_planes, -1)
            self.conv1x1_teo_sby = SeqConv3x3('conv1x1TeagerSobely', self.inp_planes, self.out_planes, -1)
            self.conv1x1_lpl = SeqConv3x3('conv1x1-laplacian', self.inp_planes, self.out_planes, -1)

        if self.act_type == 'prelu':
            self.act = nn.PReLU(num_parameters=self.out_planes)
        elif self.act_type == 'relu':
            self.act = nn.ReLU(inplace=True)
        elif self.act_type == 'rrelu':
            self.act = nn.RReLU(lower=-0.05, upper=0.05)
        elif self.act_type == 'softplus':
            self.act = nn.Softplus()
        elif self.act_type == 'linear':
            pass
        else:
            raise ValueError('The type of activation if not support!')
    
    # Laplacian Pyramid

    def lap_pyr(self, x):
        """
        Memory-efficient Laplacian pyramid fusion.
        Math-equivalent to the original implementation.
        """

        # Level 1 (full resolution)
        out = self.conv1x1_lpl(x)

        # Level 2
        x2 = F.avg_pool2d(x, kernel_size=2, stride=2)
        L2 = self.conv1x1_lpl(x2)
        L2 = F.interpolate(L2, size=x.shape[-2:], mode='bilinear', align_corners=False)
        out = out + 0.25 * L2

        # Free early
        del x2, L2

        # Level 3
        x3 = F.avg_pool2d(x, kernel_size=4, stride=4)
        L3 = self.conv1x1_lpl(x3)
        L3 = F.interpolate(L3, size=x.shape[-2:], mode='bilinear', align_corners=False)
        out = out + 0.0625 * L3

        del x3, L3

        return out

    # -------------------------------------------------------------
    # Forward
    # -------------------------------------------------------------

    def forward(self, x):
        if self.deploy:
            y = self.rep_conv(x)
        elif self.gv:
            y = self.rep_conv(x)     + \
                self.conv1x1_teo_sbx(x) + \
                self.conv1x1_teo_sby(x) + \
                self.lap_pyr(x) + x
        else:
            y = self.rep_conv(x)     + \
                self.conv1x1(x)     + \
                self.conv1x1_teo_sbx(x) + \
                self.conv1x1_teo_sby(x) + \
                self.lap_pyr(x)

            if self.with_idt:
                y += x
            if self.with_13:
                y += self.conv1x1_3x3(x)

        if self.act_type != 'linear':
            y = self.act(y)
        return y

    def switch_to_gv(self):
        if self.gv:
            return
        self.gv = True

        K0, B0 = self.rep_conv.weight, self.rep_conv.bias # Base conv
        K1, B1 = self.conv1x1_3x3.rep_params() # 1x1->3x3 (standard)
        K5, B5 = multiscale(self.conv1x1.weight,3), self.conv1x1.bias # 1x1 -> 3x3

        RK, RB = (K0+K5), (B0+B5)

        if self.with_13:
            RK, RB = RK + K1, RB + B1

        self.rep_conv.weight.data = RK
        self.rep_conv.bias.data = RB

        for para in self.parameters():
            para.detach_()


    def switch_to_deploy(self):

        if self.deploy:
            return
        self.deploy = True

        # Main branches
        K0, B0 = self.rep_conv.weight, self.rep_conv.bias
        K1, B1 = self.conv1x1_3x3.rep_params()
        K2, B2 = self.conv1x1_teo_sbx.rep_params()
        K3, B3 = self.conv1x1_teo_sby.rep_params()

        #------ laplacian pyramid
        K4_lvl1, B4_lvl1 = self.conv1x1_lpl.rep_params()

        # Approximate level 2 & 3 Laplacian Pyramid contributions
        # NOTE: these are approximations of down/conv/up
        K4_lvl2 = 0.25 * K4_lvl1
        B4_lvl2 = 0.25 * B4_lvl1

        K4_lvl3 = 0.0625 * K4_lvl1
        B4_lvl3 = 0.0625 * B4_lvl1

        K5, B5 = multiscale(self.conv1x1.weight,3), self.conv1x1.bias

        # Final collapsed Laplacian Pyramid kernel
        K_LPL = K4_lvl1 + K4_lvl2 + K4_lvl3
        B_LPL = B4_lvl1 + B4_lvl2 + B4_lvl3
        #------
        if self.gv:
            RK, RB = (K0+K2+K3+K_LPL), (B0+B2+B3+B_LPL)
        else:
            RK, RB = (K0+K2+K3+K_LPL+K5), (B0+B2+B3+B_LPL+B5)
            if self.with_13:
                RK, RB = RK + K1, RB + B1
        if self.with_idt:
            device = RK.get_device()
            if device < 0:
                device = None
            K_idt = torch.zeros(self.out_planes, self.out_planes, 3, 3, device=device)
            for i in range(self.out_planes):
                K_idt[i, i, 1, 1] = 1.0
            B_idt = 0.0
            RK, RB = RK + K_idt, RB + B_idt

        # --------------------
        # REPLACE MODULE
        # --------------------

        self.rep_conv = nn.Conv2d(in_channels=self.inp_planes, out_channels=self.out_planes, kernel_size=3, stride=1,
                                      padding=1, bias=True)
        self.rep_conv.weight.data = RK
        self.rep_conv.bias.data = RB

        for para in self.parameters():
            para.detach_()

        # --------------------
        # REMOVE BRANCHES
        # --------------------

        #self.__delattr__('conv3x3')
        self.__delattr__('conv1x1_3x3')
        self.__delattr__('conv1x1')
        self.__delattr__('conv1x1_teo_sbx')
        self.__delattr__('conv1x1_teo_sby')
        self.__delattr__('conv1x1_lpl')


class MFDB_deploy(nn.Module):
    def __init__(self, inp_planes, out_planes):
        super(MFDB_deploy, self).__init__()

        self.rep_conv = nn.Conv2d(in_channels=inp_planes, out_channels=out_planes, kernel_size=3, stride=1,
                                      padding=1, bias=True)

        self.act = nn.PReLU(num_parameters=out_planes)

    def forward(self, x):
        y = self.rep_conv(x)
        y = self.act(y)

        return y


